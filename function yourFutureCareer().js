function yourFutureCareer(){
    var career;
    if (Math.random(career) <= 0.32)
        return  "FrontEnd Developer";
    else if (Math.random(career) <= 0.65)
        return  'Full-Stack Developer';
    else
        return "BackEnd Developer";
}
